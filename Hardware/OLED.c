/**
  * OLED显存数组
  * 所有的显示函数，都只是对此显存数组进行读写
  * 随后调用OLED_Update函数或OLED_UpdateArea函数
  * 才会将显存数组的数据发送到OLED硬件，进行显示
  */
uint8_t OLED_DisplayBuf[8][128];

/*********************全局变量*/


/*通信协议*********************/
/**
  * 函    数：OLED写命令
  * 参    数：Command 要写入的命令值，范围：0x00~0xFF
  * 返 回 值：无
  * 注：江协在写封装的时候将接收应答这个函数写入了I2C里面，所有此函数略有不同
  */
void OLED_WriteCommand(uint8_t Command)
{
    MyI2C_Start();
    MyI2C_SendByte(0x78); //发送从机地址 011110 0 0
    MyI2C_ReceiveAck();
    MyI2C_SendByte(0x00); //Control Byte
    MyI2C_ReceiveAck();
    MyI2C_SendByte(Command);  //Data Byte
    MyI2C_ReceiveAck();
    MyI2C_Stop();
}

/**
  * 函    数：OLED写数据
  * 参    数：Data 要写入数据的起始地址
  * 参    数：Count 要写入数据的数量
  * 返 回 值：无
  */
void OLED_WriteData(uint8_t *Data, uint8_t Count)
{
    uint8_t i;
    MyI2C_Start();
    MyI2C_SendByte(0x78); //发送从机地址 011110 0 0
    MyI2C_ReceiveAck();
    MyI2C_SendByte(0x40); //Control Byte  DC位为1
    MyI2C_ReceiveAck();
    for (i = 0; i < Count; i ++)
	{
		MyI2C_SendByte(Data[i]);	//依次发送Data的每一个数据
        MyI2C_ReceiveAck();
	}
    MyI2C_Stop();
}

