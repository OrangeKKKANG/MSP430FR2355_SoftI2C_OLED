/**
  * 函    数：次方函数
  * 参    数：X 底数
  * 参    数：Y 指数
  * 返 回 值：等于X的Y次方
  * 注意点：MSP430是16位机，使用32位运算会卡死，所以这里修改了X,Y和返回值的类型，从uint32_t改为uint16_t
  */
uint16_t OLED_Pow(uint16_t X, uint16_t Y)
{
	uint16_t Result = 1;	//结果默认为1
	while (Y --)			//累乘Y次
	{
		Result *= X;		//每次把X累乘到结果上
	}
	return Result;
}

